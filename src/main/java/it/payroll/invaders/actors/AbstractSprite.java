package it.payroll.invaders.actors;

import it.payroll.invaders.actors.Sprite;

abstract class AbstractSprite implements Sprite {

    private int x;
    private int y;
    private int width;
    private int height;

    protected AbstractSprite(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    @Override
    public int getX() {
        return this.x;
    }

    @Override
    public void setX(int x) {
        this.x = x;
    }

    @Override
    public int getY() {
        return this.y;
    }

    @Override
    public void setY(int y) {
        this.y = y;
    }

    @Override
    public int getHeight() {
        return this.height;
    }

    @Override
    public int getWidth() {
        return this.width;
    }

}
